export interface KRA {
  id: string;
  name: string;
  description: string;
  department: string;
  weight: number; // percentage
  measurementCriteria: string;
  status: 'Active' | 'Inactive';
  createdDate: string;
  goals: number; // count of goals linked
}

export interface Goal {
  id: string;
  title: string;
  description: string;
  employeeId: string;
  employeeName: string;
  department: string;
  kraId: string;
  kraName: string;
  type: 'Individual' | 'Team' | 'Organizational';
  measurementCriteria: string;
  targetValue: string;
  currentValue: string;
  progress: number; // 0-100
  startDate: string;
  endDate: string;
  weight: number; // percentage
  priority: 'Critical' | 'High' | 'Medium' | 'Low';
  status: 'Not Started' | 'In Progress' | 'On Track' | 'At Risk' | 'Completed' | 'Overdue' | 'Cancelled';
  assignedBy: string;
  assignedDate: string;
  lastUpdated: string;
  milestones?: GoalMilestone[];
  updates?: GoalUpdate[];
}

export interface GoalMilestone {
  id: string;
  title: string;
  targetDate: string;
  status: 'Pending' | 'Completed' | 'Overdue';
  completedDate?: string;
}

export interface GoalUpdate {
  id: string;
  date: string;
  updatedBy: string;
  comment: string;
  progress: number;
  attachments?: string[];
}

